#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;

	i = 0;
	if (!s)
		return ;
	if (s[i] != '\0' && *f != NULL)
	{
		while (s[i])
		{
			f(&s[i]);
			i++;
		}
	}
}
